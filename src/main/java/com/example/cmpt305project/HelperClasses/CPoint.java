package com.example.cmpt305project.HelperClasses;

public class CPoint {

        double x, y;

        public CPoint(double x, double y) {
            this.x = x;
            this.y = y;
        }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
